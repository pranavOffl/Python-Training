# Arithmetic Operators

a, b = 5, 10
print(f'a + b = {a + b}')
print(f'a - b = {a - b}')
print(f'a / b = {a / b}')
print(f'a // b = {a // b}')
print(f'a * b = {a * b}')
print(f'a % b = {a % b}')
print(f'a ** b = {a ** b}')

# Assignment Operators

x = 5
x += 3
x -= 2
x *= 1
x /= 4
x %= 7
x //= 8
x **= 2

# Comparison Operators

x = 5
y = 3

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

# Logical Operators

x = 6

print(x > 0 and x < 10)
print(x > 0 or x < 10)
print(not(x < 0))